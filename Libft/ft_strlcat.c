#include "libft.h"
#include <unistd.h>

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dlen;
	size_t	slen;
	size_t	res;

	dlen = ft_strlen(dst);
	slen = ft_strlen(src);
	if (dlen < size)
		res = dlen + slen;
	else
		res = slen + size;
	if (size == 0)
		return (res);
	i = 0;
	while (src[i] && dlen + i < size - 1)
	{
		dst[dlen + i] = src[i];
		i++;
	}
	dst[dlen + i] = '\0';
	return (res);
}
